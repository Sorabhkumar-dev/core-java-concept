/*
there are five premitive types of variables in java
*byte
*short
*int
*long
*float
*double
*String(String is a class that treat as variable)
*/
public class VariablesInJava{
	public static void main(String[] args) {
		//byte type variable
		byte num1=12;
		System.out.println("This byte type variable in java "+num1+"\n");
	//this short type variable
	short num2=6222;
	System.out.println("This short type variable in java "+num2+"\n");
	//this integer type variable
	int num3=37666523;
	System.out.println("this integer type variable in java "+num3+"\n");
	//this long type variable
	long num4=273763l;
	System.out.println("this long type variable in java "+num4+"\n");
	//this long type variable
	float num5=2653.45f;
	System.out.println("This float type variable in java "+num5+"\n");
	//this double type variable
	double num6=7537584485.752623d;
	System.out.println("This double type variable "+num6+"\n");
	//String is class that used to store sequence of charcters
	String name="Sorabh kumar";
	System.out.println("This String class Object "+name);
	}
}